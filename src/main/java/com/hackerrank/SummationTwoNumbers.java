package com.hackerrank;

public class SummationTwoNumbers {

    public static int summationTwoNumbers(int n1, int n2) {
        return n1 + n2;
    }

    public static void main(String[] args) {
        System.out.println(summationTwoNumbers(30000, 2));
        //System.out.println(Integer.MAX_VALUE);
    }
}