package com.hackerrank;

public class MathMethods {
    public static int summationTwoNumbers(int n1, int n2) {
        return n1 + n2;
    }

    public static int subtractionTwoNumbers(int n3, int n4) {
        return n3 - n4;
    }

    public static int multiplicationTwoNumbers(int n5, int n6) {
        return n5 * n6;
    }

    public static int divisionTwoNumbers(int n7, int n8) {
        return n7 / n8;
    }
}


